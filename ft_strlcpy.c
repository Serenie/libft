#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t src_size;

	i = 0;
	if (!dst && !src)
		return (0);
	while (src[i] != '\0' && i + 1 < dstsize)
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize > 0)
		dst[i] = '\0';
	src_size = 0;
	while (src[src_size] != '\0')
	{
		src_size++;
	}
	return (src_size);
}
